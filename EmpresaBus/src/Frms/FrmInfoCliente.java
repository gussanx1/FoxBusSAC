    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        // TODO add your handling code here:
        String calidad="";
        String dni = txtDni.getText();
        String nombre = txtNombre.getText();
        String telefono = txtTelefono.getText();
        String origenDestino = cboOrigenDestino.getSelectedItem().toString();
        if(rbEstandar.isSelected())
        calidad="Estandar";
        else if(rbVip.isSelected())
        calidad="VIP";
        String dia = cboDia.getSelectedItem().toString();
        String hora = cboHora.getSelectedItem().toString();

        InfoCliente x = new InfoCliente (dni, nombre, telefono, origenDestino, calidad, dia, hora);
        a.agregar(x);
        a.mostrar(modelo);
        limpiarTextos();
    }//GEN-LAST:event_btnGuardarActionPerformed

